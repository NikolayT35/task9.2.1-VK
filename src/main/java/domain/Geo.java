package domain;

public class Geo {
    private String type;
    private String coordinates;
    private Object palace;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getCoordinates() {
        return coordinates;
    }

    public void setCoordinates(String coordinates) {
        this.coordinates = coordinates;
    }

    public Object getPalace() {
        return palace;
    }

    public void setPalace(Object palace) {
        this.palace = palace;
    }
}
